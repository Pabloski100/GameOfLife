arma::umat rotate(arma::umat& mat)
{
    arma::umat rotated(mat.n_cols, mat.n_rows);

    for (int i = 0; i < mat.n_rows; i++)
    {
        rotated.col(i) = mat.row(mat.n_rows - i - 1).as_col();
    }
    
    return rotated;
}

